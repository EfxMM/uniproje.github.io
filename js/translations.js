// Dil deƒüi≈ütirme fonksiyonu - Smooth transition ile
function changeLanguage(lang) {
    if (!translations[lang]) {
        console.error('Language not found:', lang);
        return;
    }
    
    const t = translations[lang];
    
    // Fade out animasyonu ba≈ülat
    document.body.style.opacity = '0';
    document.body.style.transition = 'opacity 0.3s ease-in-out';
    
    // 300ms sonra i√ßeriƒüi g√ºncelle
    setTimeout(() => {
        // HTML lang attribute
        document.documentElement.lang = lang;
        
        // LTR for all languages
        document.documentElement.dir = 'ltr';
        document.body.classList.remove('rtl');
        
        // LocalStorage'a kaydet
        localStorage.setItem('selectedLanguage', lang);
        
        // Quiz manager'ƒ± yeniden ba≈ülat (eƒüer varsa)
        if (typeof quizManager !== 'undefined') {
            quizManager.questions = [...getQuizData()];
            // Eƒüer quiz aktifse, ba≈üa d√∂n
            if (!document.getElementById('quizStart').classList.contains('hidden')) {
                // Quiz ba≈ülangƒ±√ß ekranƒ±ndaysa, bir ≈üey yapma
            } else if (!document.getElementById('quizGame').classList.contains('hidden')) {
                // Quiz oynanƒ±yorsa, ba≈üa d√∂n
                document.getElementById('quizStart').classList.remove('hidden');
                document.getElementById('quizGame').classList.add('hidden');
                document.getElementById('quizResults').classList.add('hidden');
            }
        }
        
        // Scenario manager'ƒ± yeniden ba≈ülat (eƒüer varsa)
        if (typeof scenarioManager !== 'undefined') {
            scenarioManager.scenarios = [...getScenariosData()];
            // Eƒüer senaryo aktifse, se√ßim ekranƒ±na d√∂n
            if (document.getElementById('scenariosPage').classList.contains('active')) {
                scenarioManager.showScenarioSelection();
            }
        }
        
        // T√ºm statik i√ßerikleri g√ºncelle
        updateStaticContent(t);
    
        // Eƒüer bilgi sayfasƒ± aktifse, dinamik i√ßerikleri yeniden y√ºkle
        if (document.getElementById('infoPage').classList.contains('active')) {
            loadInfoContent();
        }
        
        // Eƒüer ilerleme sayfasƒ± aktifse, yeniden y√ºkle
        if (document.getElementById('progressPage').classList.contains('active')) {
            loadProgressPage();
        }
        
        // Eƒüer timeline sayfasƒ± aktifse, hastalƒ±k listesini yeniden y√ºkle
        if (document.getElementById('timelinePage').classList.contains('active')) {
            if (typeof loadDiseaseOptions === 'function') {
                loadDiseaseOptions();
                // Eƒüer bir hastalƒ±k se√ßiliyse, onu yeniden g√∂ster
                const diseaseSelect = document.getElementById('diseaseSelect');
                if (diseaseSelect && diseaseSelect.value) {
                    showTimeline(diseaseSelect.value);
                }
            }
        }
        
        // Fade in animasyonu
        setTimeout(() => {
            document.body.style.opacity = '1';
        }, 50);
    }, 300);
}

