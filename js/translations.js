// Aktif dili al
function getCurrentLanguage() {
    return localStorage.getItem('selectedLanguage') || 'tr';
}

// √áeviri al
function getTranslation(key) {
    const lang = getCurrentLanguage();
    return translations[lang][key] || translations['tr'][key] || key;
}

// Sayfa y√ºklendiƒüinde dil ayarƒ±nƒ± uygula
document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('selectedLanguage') || 'tr';
    changeLanguage(savedLang);
    
    // Dil se√ßici varsa aktif dili i≈üaretle
    const langSelector = document.querySelector(`[data-lang="${savedLang}"]`);
    if (langSelector) {
        document.querySelectorAll('[data-lang]').forEach(btn => btn.classList.remove('active'));
        langSelector.classList.add('active');
    }
});
